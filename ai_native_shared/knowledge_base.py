def _normalize_text(text: str) -> str:
    return re.sub(r"\s+", " ", text.strip())


def _tokenize_for_match(text: str) -> set[str]:
    """提取中文2-4字片段和英文/数字词，用于轻量关键词匹配."""
    normalized = _normalize_text(text).lower()
    tokens = set(re.findall(r"[a-z0-9]+", normalized))
    chinese_blocks = re.findall(r"[一-鿿]+", normalized)
    for block in chinese_blocks:
        for size in (2, 3, 4):
            if len(block) < size:
                continue
            tokens.update(block[i : i + size] for i in range(len(block) - size + 1))
    return tokens


def _keyword_score(text: str, query: str) -> float:
    """基于关键词重叠的简单评分，0-1范围."""
    text_lower = _normalize_text(text).lower()
    query_lower = _normalize_text(query).lower()

    query_chars = set(query_lower)
    text_chars = set(text_lower)
    char_overlap = len(query_chars & text_chars) / max(len(query_chars), 1)

    query_tokens = _tokenize_for_match(query_lower)
    if query_tokens:
        text_tokens = _tokenize_for_match(text_lower)
        token_overlap = len(query_tokens & text_tokens) / max(len(query_tokens), 1)
    else:
        token_overlap = 0

    return 0.35 * char_overlap + 0.65 * token_overlap


def retrieve_knowledge(query: str, top_k: int = 3) -> list[dict[str, str]]:
    """基于关键词匹配检索知识片段，返回带score的知识引用列表."""
    if not query or not query.strip():
        return []

    scored: list[tuple[float, KnowledgeChunk]] = []
    for chunk in KNOWLEDGE_CHUNKS:
        searchable_text = f"{chunk.source} {chunk.title} {chunk.text}"
        score = _keyword_score(searchable_text, query)
        if score > 0:
            scored.append((score, chunk))

    scored.sort(key=lambda x: x[0], reverse=True)

    results = []
    for score, chunk in scored[:top_k]:
        idx = KNOWLEDGE_CHUNKS.index(chunk) + 1
        results.append({
            "source": chunk.source,
            "chunk_id": f"KB-{idx:03d}",
            "title": chunk.title,
            "text": chunk.text,
            "score": round(score, 3),
        })

    return results


def has_sufficient_evidence(hits: list[dict[str, str]]) -> bool:
    """判断检索结果是否足以支撑回答。最高分 >= 0.15 即为充足."""
    if not hits:
        return False
    max_score = max(float(h.get("score", 0)) for h in hits)
    return max_score >= 0.15
